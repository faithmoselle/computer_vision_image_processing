# Compute and display quality metrics for different filters
def compute_metrics(original, filtered, name):
    mse = metrics.mean_squared_error(original, filtered)
    psnr = metrics.peak_signal_noise_ratio(original, filtered)
    ssim_value = ssim(original, filtered, data_range=original.max() - original.min())
    print(f"{name} - MSE: {mse:.4f}, PSNR: {psnr:.2f} dB, SSIM: {ssim_value:.4f}")

compute_metrics(img_original, median_filtered, "Standard Median Filter")
compute_metrics(img_original, adaptive_filtered, "Adaptive Median Filter")
