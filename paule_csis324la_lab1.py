import numpy as np
from matplotlib import pyplot as plt
import cv2
from skimage import metrics
from skimage.metrics import structural_similarity as ssim

def show(img):
    plt.imshow(img, cmap='gray')
    plt.xticks([])
    plt.yticks([])

def standard_median_filter(image, kernel_size=3):
    return cv2.medianBlur(image, kernel_size)

def adaptive_median_filter(image, max_kernel_size=7):
    h, w = image.shape
    new_img = np.copy(image)

    for i in range(h):
        for j in range(w):
            k = 3  # Start with 3x3 kernel
            while k <= max_kernel_size:
                i_min = max(i - k // 2, 0)
                i_max = min(i + k // 2 + 1, h)
                j_min = max(j - k // 2, 0)
                j_max = min(j + k // 2 + 1, w)

                region = image[i_min:i_max, j_min:j_max]
                z_min = np.min(region)
                z_max = np.max(region)
                z_med = np.median(region)
                z_xy = image[i, j]

                if z_min < z_med < z_max:
                    if z_min < z_xy < z_max:
                        new_img[i, j] = z_xy
                    else:
                        new_img[i, j] = z_med
                    break
                else:
                    k += 2  # Increase kernel size

    return new_img

def alpha_trimmed_mean_filter(image, kernel_size=3, d=2):
    h, w = image.shape
    pad_size = kernel_size // 2
    padded_img = np.pad(image, pad_size, mode='reflect')
    new_img = np.zeros_like(image)

    for i in range(h):
        for j in range(w):
            region = padded_img[i:i+kernel_size, j:j+kernel_size].flatten()
            sorted_region = np.sort(region)
            trimmed_region = sorted_region[d//2 : -d//2] if d < kernel_size**2 else sorted_region
            new_img[i, j] = np.mean(trimmed_region)

    return new_img.astype(np.uint8)

