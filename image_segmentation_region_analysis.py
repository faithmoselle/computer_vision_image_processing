import cv2
import numpy as np
import matplotlib.pyplot as plt
from google.colab.patches import cv2_imshow # For image display in Colab


# ============================================================================
# TASK 1: GLOBAL vs LOCAL THRESHOLDING
# ============================================================================
def thresholding_demo(image_path):
    """
    Compares Otsu's (global) thresholding and Adaptive (local) thresholding.
    Displays original, Otsu, and Adaptive results side-by-side.
    """
    # Load image in grayscale
    img = cv2.imread(image_path, cv2.IMREAD_GRAYSCALE)
    if img is None:
        print(f"Error: Could not load image from {image_path}")
        return

    # Global Thresholding (Otsu's Method - automatically calculates one threshold)
    _, global_thresh = cv2.threshold(img, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)

    # Local Adaptive Thresholding (threshold varies by local pixel neighborhood)
    # Uses the mean of an 11x11 block, subtracts a constant (2)
    local_thresh = cv2.adaptiveThreshold(img, 255, cv2.ADAPTIVE_THRESH_MEAN_C,
                                         cv2.THRESH_BINARY, 11, 2)

    # Display results
    titles = ['Original (Grayscale)', 'Global (Otsu)', 'Local (Adaptive)']
    images = [img, global_thresh, local_thresh]

    plt.figure(figsize=(12, 4))
    for i in range(3):
        plt.subplot(1, 3, i+1)
        plt.imshow(images[i], cmap='gray')
        plt.title(titles[i])
        plt.axis('off')
    plt.tight_layout()
    plt.show()


# ============================================================================
# TASK 2: EDGE DETECTION (CANNY)
# ============================================================================
def edge_segmentation(image_path):
    """
    Applies Canny edge detection to an image.
    Displays the original and the resulting edge map.
    """
    # Load original color image
    img = cv2.imread(image_path, cv2.IMREAD_COLOR)
    if img is None:
        print(f"Error: Could not load image from {image_path}")
        return

    # Convert to grayscale (edges are detected on intensity)
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

    # Apply Canny edge detector (thresholds: 100 and 200)
    edges = cv2.Canny(gray, 100, 200)

    # Display using Colab-compatible functions
    print("Original Image:")
    cv2_imshow(img)
    print("\nEdge Segmentation Result (White pixels are edges):")
    cv2_imshow(edges)


