# TASK 3: REGION-BASED SEGMENTATION (CONNECTED COMPONENTS)
# ============================================================================
def region_based_segmentation(image_path):
    """
    Segments an image using binary thresholding and connected components.
    Displays a color-coded map of the detected regions.
    """
    # Load image in grayscale
    img = cv2.imread(image_path, cv2.IMREAD_GRAYSCALE)
    if img is None:
        print(f"Error: Could not load image from {image_path}")
        return

    # Create a binary image using a simple threshold (127)
    _, binary = cv2.threshold(img, 127, 255, cv2.THRESH_BINARY)

    # Find connected components (blobs of white pixels)
    # num_labels includes the background (label 0)
    num_labels, labels = cv2.connectedComponents(binary)

    # Display the result, color-coding each region
    # Subtract 1 from num_labels to exclude the background
    plt.figure(figsize=(8, 6))
    plt.imshow(labels, cmap='jet')
    plt.title(f'Segmented Regions ({num_labels - 1} foreground regions)', fontsize=14)
    plt.axis('off')
    plt.colorbar(label='Region ID')
    plt.tight_layout()
    plt.show()


