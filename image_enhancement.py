def histogram_equalization(image_path):
    """
    Apply histogram equalization and display both images and their histograms.
    
    Error corrections applied:
    - Fixed indentation
    - Added plt.tight_layout() for better plot readability
    - Corrected filename to 'input.png'
    """
    # Read image in grayscale
    img = cv2.imread(image_path, cv2.IMREAD_GRAYSCALE)
    if img is None:
        print("Error: Could not read image.")
        return
    
    # Apply histogram equalization
    enhanced_img = cv2.equalizeHist(img)
    
    # Compute histograms (pixel intensity distribution)
    original_hist = cv2.calcHist([img], [0], None, [256], [0, 256])
    enhanced_hist = cv2.calcHist([enhanced_img], [0], None, [256], [0, 256])
    
    # Display 2x2 grid: images top, histograms bottom
    plt.figure(figsize=(12, 6))
    
    # Top-Left: Original Image
    plt.subplot(2, 2, 1)
    plt.imshow(img, cmap='gray')
    plt.title('Original Image')
    plt.axis('off')
    
    # Top-Right: Enhanced Image
    plt.subplot(2, 2, 2)
    plt.imshow(enhanced_img, cmap='gray')
    plt.title('Enhanced Image')
    plt.axis('off')
    
    # Bottom-Left: Original Histogram
    plt.subplot(2, 2, 3)
    plt.plot(original_hist, color='black')
    plt.title('Original Histogram')
    plt.xlim([0, 256])
    plt.xlabel('Pixel Intensity')
    plt.ylabel('Frequency')
    
    # Bottom-Right: Enhanced Histogram
    plt.subplot(2, 2, 4)
    plt.plot(enhanced_hist, color='black')
    plt.title('Enhanced Histogram (Equalized)')
    plt.xlim([0, 256])
    plt.xlabel('Pixel Intensity')
    plt.ylabel('Frequency')
    
    plt.tight_layout()  # Improves spacing between subplots
    plt.show()

# Execute Task 2
histogram_equalization("/content/input.png")


