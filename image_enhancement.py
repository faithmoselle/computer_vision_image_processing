def adaptive_wiener_filter(image_path):
    """
    Apply Wiener filter for adaptive noise reduction.
    
    Error corrections applied:
    - Converted image to float32 before filtering (Wiener expects float)
    - Clipped filtered values to [0, 255] range
    - Converted back to uint8 for proper display
    - Added proper filename 'input.png'
    
    Wiener filter adapts to local image statistics:
    - Low noise areas: preserves original values
    - High noise areas: applies stronger smoothing
    """
    # Read image in grayscale
    img = cv2.imread(image_path, cv2.IMREAD_GRAYSCALE)
    if img is None:
        print("Error: Could not read image.")
        return
    
    # Convert to float32 (Wiener filter requires float input)
    img_float = img.astype(np.float32)
    
    # Apply Wiener filter with 5x5 kernel
    # Filter adapts based on local variance
    wiener_filtered = wiener(img_float, (5, 5))
    
    # Clip values to valid range [0, 255] and convert back to uint8
    wiener_filtered = np.clip(wiener_filtered, 0, 255).astype(np.uint8)
    
    # Display original vs Wiener filtered images
    plt.figure(figsize=(10, 5))
    
    plt.subplot(1, 2, 1)
    plt.imshow(img, cmap='gray')
    plt.title('Original Image (with noise)')
    plt.axis('off')
    
    plt.subplot(1, 2, 2)
    plt.imshow(wiener_filtered, cmap='gray')
    plt.title('Wiener Filter Applied')
    plt.axis('off')
    
    plt.show()

# Execute Task 3
adaptive_wiener_filter("/content/input.png")


