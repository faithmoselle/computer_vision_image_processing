def enhance_contrast(image_path):
    """
    Enhance image contrast using histogram equalization.
    
    Error corrections applied:
    - Fixed indentation (code block alignment)
    - Corrected filename to 'input.png'
    - Added error handling for missing image
    """
    # Read image in grayscale
    img = cv2.imread(image_path, cv2.IMREAD_GRAYSCALE)
    if img is None:
        print("Error: Could not read image.")
        return
    
    # Apply histogram equalization (spreads pixel intensities)
    enhanced_img = cv2.equalizeHist(img)
    
    # Display original vs enhanced images
    plt.figure(figsize=(10, 5))
    
    plt.subplot(1, 2, 1)
    plt.imshow(img, cmap='gray')
    plt.title('Original Image')
    plt.axis('off')
    
    plt.subplot(1, 2, 2)
    plt.imshow(enhanced_img, cmap='gray')
    plt.title('Enhanced Image (Histogram Equalization)')
    plt.axis('off')
    
    plt.show()
    return enhanced_img

