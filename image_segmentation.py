def edge_segmentation(image_path):
    """
    Detect edges using Canny edge detector.
    
    Canny Algorithm Steps:
    1. Gaussian blur to reduce noise
    2. Gradient calculation (Sobel)
    3. Non-maximum suppression
    4. Hysteresis thresholding (minVal=100, maxVal=200)
    
    NOTE: In Google Colab, use matplotlib instead of cv2.imshow()
    """
    # Load image in color and grayscale
    image = cv2.imread(image_path, cv2.IMREAD_COLOR)
    if image is None:
        print("Error: Could not read image.")
        return
    
    # Convert to grayscale (edges detected on intensity)
    gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    
    # Apply Canny Edge Detection
    # minVal=100, maxVal=200 (edges between these thresholds are kept)
    edges = cv2.Canny(gray, 100, 200)
    
    # Convert BGR to RGB for proper matplotlib display
    image_rgb = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
    
    # Display using matplotlib (works in Colab)
    plt.figure(figsize=(12, 5))
    
    plt.subplot(1, 2, 1)
    plt.imshow(image_rgb)
    plt.title('Original Image')
    plt.axis('off')
    
    plt.subplot(1, 2, 2)
    plt.imshow(edges, cmap='gray')
    plt.title('Edge Segmentation (Canny)')
    plt.axis('off')
    
    plt.suptitle('Edge Detection Results', fontsize=14)
    plt.show()
    
    return edges

