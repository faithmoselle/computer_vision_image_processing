def thresholding_demo(image_path):
    """
    Compare global (Otsu) and local (adaptive) thresholding.
    
    Global Thresholding (Otsu):
    - Single threshold value for entire image
    - Best for uniform lighting conditions
    
    Local Thresholding (Adaptive):
    - Different thresholds for different regions
    - Better for uneven lighting
    """
    # Load image in grayscale
    image = cv2.imread(image_path, cv2.IMREAD_GRAYSCALE)
    if image is None:
        print("Error: Could not read image.")
        return
    
    # Global Thresholding using Otsu's method
    # Automatically computes optimal threshold value
    _, global_thresh = cv2.threshold(image, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
    
    # Local (Adaptive) Thresholding
    # Uses mean of 11x11 neighborhood, C=2 constant subtracted
    local_thresh = cv2.adaptiveThreshold(
        image, 255, cv2.ADAPTIVE_THRESH_MEAN_C, 
        cv2.THRESH_BINARY, 11, 2
    )
    
    # Display results side by side
    titles = ['Original Image', 'Global Thresholding (Otsu)', 'Local (Adaptive) Thresholding']
    images = [image, global_thresh, local_thresh]
    
    plt.figure(figsize=(15, 5))
    for i in range(3):
        plt.subplot(1, 3, i + 1)
        plt.imshow(images[i], cmap='gray')
        plt.title(titles[i])
        plt.axis('off')
    
    plt.suptitle('Thresholding Comparison: Global vs Local', fontsize=14)
    plt.tight_layout()
    plt.show()
    
    return global_thresh, local_thresh

