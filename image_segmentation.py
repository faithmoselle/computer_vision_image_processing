def region_based_segmentation(image_path):
    """
    Segment image using connected components analysis.
    
    Steps:
    1. Threshold image to binary
    2. Find connected components (contiguous regions of white pixels)
    3. Color-code each region uniquely using 'jet' colormap
    
    Output: Color-coded regions with count in title
    """
    # Load image in grayscale
    image = cv2.imread(image_path, cv2.IMREAD_GRAYSCALE)
    if image is None:
        print("Error: Could not read image.")
        return
    
    # Binary thresholding (pixels > 127 become white)
    _, binary = cv2.threshold(image, 127, 255, cv2.THRESH_BINARY)
    
    # Find connected components
    # num_labels = number of regions (including background)
    # labels = matrix where each pixel has region ID
    num_labels, labels = cv2.connectedComponents(binary)
    
    # Subtract 1 to exclude background from region count
    print(f"Detected regions: {num_labels - 1} (excluding background)")
    
    # Display color-coded regions using 'jet' colormap
    plt.figure(figsize=(8, 6))
    plt.imshow(labels, cmap='jet')
    plt.title(f'Segmented Regions ({num_labels - 1} regions)', fontsize=14)
    plt.axis('off')
    plt.colorbar(label='Region ID')
    plt.tight_layout()
    plt.show()
    
    return labels, num_labels

